setTimeout(function(){
  document.querySelector(".capa-container").style.overflow = "unset";
}, 6000);

document.querySelector(".link-capa-1").addEventListener("mouseover", function(){
  var flores = document.querySelectorAll('.flor');
  [].forEach.call(flores, function(flor) {
    flor.style.opacity = "0.3";
  });
  document.querySelector(".flor-1").style.opacity = "1";
})

document.querySelector(".link-capa-1").addEventListener("mouseout", function(){
  var flores = document.querySelectorAll('.flor');
  [].forEach.call(flores, function(flor) {
    flor.style.opacity = "1";
  });
})

